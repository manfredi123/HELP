    public void mostra(GestioneFamiglieControl gestioneFamiglieControl,List<DatiComponente> famiglia){
        this.gestioneFamiglieControl = gestioneFamiglieControl;
        this.famiglia = famiglia;
        
        DefaultTableModel model = new DefaultTableModel();

        model.addColumn("Nome");
        model.addColumn("Cognome");
        model.addColumn("Et√†");
        model.addColumn("CF");
        
        for(DatiComponente d : famiglia){
            Object[] rowData = {d.nome, d.cognome, d.eta, d.CF};
            model.addRow(rowData);
        }

        this.TabellaComponenti.setModel(model);

        this.setVisible(true);

        
    }
    

