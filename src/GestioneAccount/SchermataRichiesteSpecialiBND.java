    private void removeAllRowsFromTable(JTable table) {
        this.viveriRichiesti.clear();
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        while(model.getRowCount() > 0){
            model.removeRow(0);
        }
    }

    
