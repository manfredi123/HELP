    public void mostra(GestioneMagazzinoControl gestioneMagazzinoControl, List<Viveri> datiMagazzino){
        this.gestioneMagazzinoControl = gestioneMagazzinoControl;
        this.datiMagazzino = datiMagazzino;
        DefaultTableModel model = new DefaultTableModel();
        
        model.addColumn("Polo Destinatario");
        model.addColumn("Viveri");
        model.addColumn("Attualmente In Magazzino");
        
        for (Viveri viveri : this.datiMagazzino){
            Object[] rowData = {viveri.nome, viveri.QTA};
            model.addRow(rowData);
        }

        this.TabellaViveri.setModel(model);
        this.setVisible(true);
    }
    
