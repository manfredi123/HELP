    private void EliminaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminaButtonActionPerformed
        int qta = 0;
        for(int i = 0; i<this.TabellaViveri.getRowCount(); i++){
            String tmp = this.TabellaViveri.getValueAt(i, 1).toString();
            qta = Integer.valueOf(tmp);

            if(qta < Integer.valueOf(this.TabellaViveri.getValueAt(i, 1).toString())){
                //Aggiungo un oggetto di tipo viveri alla lista dei viveri da rimuovere
                this.daRimuovere.add(new Viveri((String) this.TabellaViveri.getValueAt(i,0),(Integer) this.TabellaViveri.getValueAt(i, 1) - qta));
            }
        }

        this.gestioneMagazzinoControl.rimuovi(this.daRimuovere,true);
        this.setVisible(false);
        this.removeAllRowsFromTable(this.TabellaViveri);
        this.gestioneMagazzinoControl.createSchermataSegnalazioneErrore();
       
        this.gestioneMagazzinoControl.modificaDataScarico();
    }//GEN-LAST:event_EliminaButtonActionPerformed

