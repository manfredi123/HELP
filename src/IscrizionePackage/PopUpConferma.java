    private void confermaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confermaButtonActionPerformed
        this.setVisible(false);
        if(this.tipologia.equals("Azienda")){
            this.iscrizioneAzienda.setVisible(false);
             this.loginControl.schermata.mostra();
        }
        else if(this.tipologia.equals("Rappresentante")){
            this.iscrizioneRappresentante.setVisible(false);
            this.loginControl.schermata.mostra();
        }
        else if(this.tipologia.equals("Diocesi")){
            this.iscrizioneDiocesi.setVisible(false);
            this.loginControl.schermata.mostra();
        }
    }//GEN-LAST:event_confermaButtonActionPerformed
    
    
